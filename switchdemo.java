package knightingaleday5;

public class switchdemo {

	public static void main(String[] args) {
		 int i=300;
				 
			switch(i)
			{		
				case 100:{
					System.out.println("the i value is 100");
					break;
				}
				case 200:{
					System.out.println("the i value is 200");
					break;
				}
				case 300:{
					System.out.println("the i value is 300");
					break;
				}
				default:{
					System.out.println("no condition is satisfied....");
				}
			}
			
	}
}	
			

